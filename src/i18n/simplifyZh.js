export function toSimplified(text) {
  if (typeof text !== "string") {
    return text;
  }

  let result = text;

  PHRASE_MAP.forEach(([traditional, simplified]) => {
    result = result.split(traditional).join(simplified);
  });

  return result
    .split("")
    .map((char) => CHAR_MAP[char] ?? char)
    .join("");
}

export function toSimplifiedLocale(value) {
  if (typeof value === "string") {
    return toSimplified(value);
  }

  if (Array.isArray(value)) {
    return value.map(toSimplifiedLocale);
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([key, nestedValue]) => [
        key,
        toSimplifiedLocale(nestedValue),
      ]),
    );
  }

  return value;
}
